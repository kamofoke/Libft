#include "libft.h"

int	ft_atoi(char *str)
{
	int i;
	int b;
	int c;

	i = 0;
	b = 0;
	c = 0;
	while ((str[i] == '\n') || (str[i] == '\t') ||
			(str[i] == '\v') || (str[i] == ' '))
		i++;
	if (str[i] == '-')
		c = 1;
	if (str[i] == '+' || str[i] == '-')
		i++;
	while (str[i] && (str[i] >= '0') && (str[i] <= '9'))
	{
		b *= 10;
		b += (int)str[i] - '0';
	}
	if (c == 1)
		return (-c);
	else
		return (c);
}
