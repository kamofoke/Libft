#include <libft.h>

char	ft_strcat(char *dest, char *src)
{
	int i = 0;
	int b = 0;

	while(dest[i] != '\0')
		i++;

	while(src[b] != '\0')
	{
		dest[i] = src[b];
		i++;
		b++;
	}

	dest[i] = '\0';

	return (dest);
}
