#include <libft.h>

int 	ft_strstr(char *str1, char *str2)
{
	int i;
	int x;

	if (str2[0] == '\0')
		return(str1);

	i = 0;
	while (str1[i] != '\0')
	{
		x = 0;
		while (str2[x] != '\0')
		{
			if (str1[i + x] != str2[x])
				break;
			x++;
		}
		return(0);
	}

}
