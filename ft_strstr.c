#include "libft.h"

int 	ft_strstr(char *str1, char *str2)
{
	int i;
	int b;

	if (str2[0] == '\0')
		return(str1);

	i = 0;
	while (str1[i] != '\0')
	{
		b = 0;
		while (str2[b] != '\0')
		{
			if (str1[i + b] != str2[b])
				break;
			b++;
		}
		return(0);
	}

}
